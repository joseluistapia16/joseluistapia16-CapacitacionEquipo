    private Usuario getObject(String usuario, List<Usuario> lista) {
        Usuario obj = null;
        for (int i = 0; i < lista.size(); i++) {
            if (usuario.equals(lista.get(i).getUsuario())) {
                obj = lista.get(i);
                break;
            }
        }
        return obj;
    }

